    public function index()
    {
        $carts = Cart::where('user_id', auth()->user()->id)->get();
        $totalPrice = 0;
        foreach ($carts as $cart) {
            $totalPrice += $cart->product->price * $cart->product->stock;
        }
        return view('cart', [
            'title' => 'Cart',
            'carts' => $carts,
            'totalPrice' => $totalPrice,
        ]);
    }

